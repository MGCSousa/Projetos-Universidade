    private void efetuarRegistoDialog_registarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_efetuarRegistoDialog_registarButtonActionPerformed
        int index = tabelaUCs.getSelectedRow();
        String nomeUC = (String)tabelaUCs.getValueAt(index, 0);
        String idUC = nomeUC.substring(nomeUC.indexOf("[") + 1, nomeUC.indexOf("]"));
        String tipoTurno = (String)tabelaUCs.getValueAt(index, 1);

        boolean flag = this.facade.subscribeUC(idUC, tipoTurno);
        if (!flag)
            javax.swing.JOptionPane.showMessageDialog(this, "Já se encontra registado nessa UC.", "Registo UC", 0);
        else {
            DefaultTableModel dtm = (DefaultTableModel)uCTurnoTable.getModel();
            dtm.addRow(new Object[] {
                idUC,
                nomeUC.substring(nomeUC.indexOf("]") + 2),
                tipoTurno
            });

            /* Actualizar Map<idUC, tipoTurno> com UC que foi registada. */
            Aluno a = (Aluno)this.facade.getSessao();
            Map<String, String> turnos = a.getTurnos();
            turnos.put(idUC, tipoTurno);
            a.setTurnos(turnos);

            javax.swing.JOptionPane.showMessageDialog(this, "Registo efetuado com sucesso!", "Registo UC", 1);
        }

        //uc_comboBox_trocas.addItem(idUC + " - " + nomeUC);
    }//GEN-LAST:event_efetuarRegistoDialog_registarButtonActionPerformed

