    /**
     * Método de action performed no menu trocas.
     * Este método que comunica à Facade que uma troca foi aceite.
     * Posto isto, adiciona esse novo turno ao menu principal do Aluno.
     * 
     * @param evt 
     */
    private void trocasDialog_aceitarTrocaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_trocasDialog_aceitarTrocaButtonActionPerformed
        DefaultTableModel dtm = (DefaultTableModel)jTable2.getModel();
        int index = jTable2.getSelectedRow();
        String tipoTurnoPretendido = (String)dtm.getValueAt(index, 1);
        Integer idAlunoReq = (Integer)dtm.getValueAt(index, 0);
        String uc = (String)uc_comboBox_trocas.getSelectedItem();
        String idUC = uc.substring(0, uc.indexOf(" "));
        
        this.facade.resolverTrocaAluno(idAlunoReq, idUC, tipoTurnoPretendido);
                        
        /* Mudar Turno no Menu do Aluno após uma Troca. */
        DefaultTableModel dtm2 = (DefaultTableModel)uCTurnoTable.getModel();
        boolean flag = true;
        for (int i = 0; i < dtm2.getRowCount() && flag; i++) {
            String idUCTabela = (String)dtm2.getValueAt(i, 0);
            if (idUCTabela.equals(idUC)) {
                dtm2.setValueAt(tipoTurnoPretendido, i, 2);
                flag = false;
            }
        }
        
        /* Remove a troca da tabela. */
        dtm.removeRow(index);
    }//GEN-LAST:event_trocasDialog_aceitarTrocaButtonActionPerformed

    private void trocasDialog_efetuarTrocaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_trocasDialog_efetuarTrocaButtonActionPerformed
        efetuarTroca_Dialog.setVisible(true);
        efetuarTroca_Dialog.setLocationRelativeTo(this);

        String uc = (String)uc_comboBox_trocas.getSelectedItem();
        uc_efetuarTroca_TextField.setText(uc);

        String idUC = uc.substring(0, uc.indexOf(" "));
        Aluno a = (Aluno)this.facade.getSessao();
        String turno = a.getTurnos().get(idUC);
        turnoAtual.setText(turno);

        ArrayList<String> tipoTurnos = this.facade.getTurnosUC(idUC);
        tipoTurnos.remove(turno); //Remove o turno em que o aluno está inserido.
        tipoTurnos.forEach(s -> {
            turnoPretendido.addItem(s);
        });
    }//GEN-LAST:event_trocasDialog_efetuarTrocaButtonActionPerformed

