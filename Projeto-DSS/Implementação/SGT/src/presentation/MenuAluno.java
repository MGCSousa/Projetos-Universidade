    private void efetuarTrocaDialog_validarTrocaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_efetuarTrocaDialog_validarTrocaButtonActionPerformed
        String uc = uc_efetuarTroca_TextField.getText();
        String idUC = uc.substring(0, uc.indexOf(" "));
        String turnoActual = turnoAtual.getText();
        String turnoPret = (String)turnoPretendido.getSelectedItem();
        
        int flag = this.facade.resolveTrocaAluno(idUC, turnoActual, turnoPret);
        
        if (flag == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Erro. Turno está cheio.", "Troca de Turno", 0);
        } else {
           turnoPretendido.removeAllItems();
           efetuarTroca_Dialog.setVisible(false); 
        }
    }//GEN-LAST:event_efetuarTrocaDialog_validarTrocaButtonActionPerformed

