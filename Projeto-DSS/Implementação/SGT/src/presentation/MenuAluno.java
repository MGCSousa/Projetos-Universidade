    private void uc_comboBox_trocasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_uc_comboBox_trocasActionPerformed
        DefaultTableModel dtm = (DefaultTableModel)jTable2.getModel();
        for (int i = 0; i < dtm.getRowCount(); i++) dtm.removeRow(i);
        String uc = (String)uc_comboBox_trocas.getSelectedItem();
        try {
            String idUC = uc.substring(0, uc.indexOf(" "));
            Aluno a = (Aluno)this.facade.getSessao();
            String turnoAtual = a.getTurnos().get(idUC);
        
            ArrayList<Troca> trocas = this.facade.getTrocasUC(turnoAtual, idUC);
            trocas.forEach(t -> {
                dtm.addRow(new Object[] {
                    t.getAlunoReq(),
                    t.getTurnoAtual()
                });
            });
        } catch (NullPointerException e) { }
    }//GEN-LAST:event_uc_comboBox_trocasActionPerformed

